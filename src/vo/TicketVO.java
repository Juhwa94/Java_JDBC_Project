package vo;

public class TicketVO {

	private int num;
	private int hcount;
	private int price;
	private int total;
	private String rtime;
	private int cnum;
	
	public int getNum() {
		return num;
	}
	
	public void setNum(int num) {
		this.num = num;
	}
	
	public int getHcount() {
		return hcount;
	}
	
	public void setHcount(int hcount) {
		this.hcount = hcount;
	}
	
	public int getPrice() {
		return price;
	}
	
	public void setPrice(int price) {
		this.price = price;
	}
	
	public int getTotal() {
		return total;
	}
	
	public void setTotal(int total) {
		this.total = total;
	}
	
	public String getRtime() {
		return rtime;
	}
	
	public void setRtime(String rtime) {
		this.rtime = rtime;
	}
	
	public int getCnum() {
		return cnum;
	}
	
	public void setCnum(int cnum) {
		this.cnum = cnum;
	}

}
